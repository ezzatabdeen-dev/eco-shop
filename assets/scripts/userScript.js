  setupEventListeners() {
    document.addEventListener("click", (e) => {
      const profileToggle = e.target.closest(".profileToggle");
      if (profileToggle) {
        e.preventDefault();
        const dropdown = profileToggle.closest(".dropdownn");
        const popup = dropdown.querySelector(
          ".acsessPopupList, .dropdownn-content"
        );

        document
          .querySelectorAll(".acsessPopupList, .dropdownn-content")
          .forEach((menu) => {
            if (menu !== popup) menu.classList.remove("active");
          });

        popup.classList.toggle("active");
      }

      if (e.target.closest("#ProfileSmToggle")) {
        e.preventDefault();
        this.toggleMobileMenu();
      }

      if (e.target.closest(".closeAcsessPopupListSm")) {
        e.preventDefault();
        this.closeMobileMenu();
      }

      if (e.target.closest("#logoutBtn, .logout-mobile")) {
        e.preventDefault();
        this.logout();
      }

      if (!e.target.closest(".dropdownn, #ProfileSmToggle")) {
        document
          .querySelectorAll(".acsessPopupList, .dropdownn-content")
          .forEach((menu) => {
            menu.classList.remove("active");
          });
        this.closeMobileMenu();
      }
    });
  }

  async logout() {
    try {
      const response = await fetch("./api/logout.php");
      const data = await response.json();

      if (data.status === "success") {
        this.userData = null;
        this.clearUserWidget();
        this.renderLoggedOutUI();
        this.updateProfileIcon();
        this.updateMobileMenu();
      }
    } catch (error) {
      console.error("Logout failed:", error);
    }
  }
